# Formatted String
first = 'Lorie'
last = 'Alexan'
message = f'{first} [{last}] is a coder' # Use (f) in front of single quote so that we don't need to use (+) when we need to add a string.
print(message)
msg = first + ' [' + last + '] is a coder'
print(msg) # This is maybe the reason why we need to use formatted string.