# Enumerate
to_do = ["pray to god", "workout", "boxing training", "clean home", "water your plant"]
for i, thing in enumerate(to_do): # Use enumerate() to have 2 value in your for loop.
    print(i, thing.title()) # Use title to capital all the first letter of the word.

names = ["nosca", "firec", "lorie", "saodino"]
