# List Method.
numbers = [1, 2, 3, 4, 5]
numbers.append(6) # Use Append to add numbers.
print(numbers)
# We can also insert something in the list in any places.
numbers.insert(0, 6) # The 0 is the place where the next number will be. Use can also add any types of data includes string, float, booleans.
print(numbers)
# We can also remove something in the list.
numbers.remove(3)
print(numbers)
# We can also clear everything in the list.
numbers.clear() # it does not need any values in the list.
print(numbers)