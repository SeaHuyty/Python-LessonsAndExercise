# Arithmetic Operators
a = 3
b = 2

# (+) is a sum operator.
print(a + b)

# (-) is a subtract operator.
print(a - b)

# (*) is a multiply operator.
print(a * b)

# (/) is a divide operator which output a float number.
print(a / b)

# (//) is a divide operator which output an integer number.
print(a // b)

# This (%) display the remain after division.
print(b % a)

# This (**) mean something to the power of something. (10 ** 2) = 10^2.
print(10 ** 2)