# Number theoretic and representation functions.
a = 2.6

# Import the function first
import math

print(math.floor(2.9)) # Decrease the float number to integer.

print(math.ceil(2.6)) # Increase the float number to integer.

print(math.factorial(3)) # It calculates factorial.

print(round(a)) # It decreases or increase the number from float to integer.

print(-a) # Turn to negative value.

print(abs(-a)) # It displays the absolute value and will always be positive.